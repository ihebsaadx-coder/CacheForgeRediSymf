    //  */

    public function findAllDepart($updated_at)
    {
        $qb = $this->createQueryBuilder('d')
            ->andWhere('d.updatedAt != :date')
            ->setParameter('date', $updated_at);
        return $qb->getQuery()
            ->getResult();
    }


